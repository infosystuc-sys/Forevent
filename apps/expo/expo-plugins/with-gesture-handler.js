/**
 * Expo config plugin para incluir react-native-gesture-handler cuando
 * el autolinking de React Native CLI no lo detecta (monorepos pnpm).
 *
 * El PackageList generado NO incluye RNGestureHandlerPackage, lo que causa
 * "RNGestureHandlerRootView was not found in the UIManager".
 *
 * Este plugin:
 * 1. Añade el proyecto a settings.gradle
 * 2. Añade la dependencia al app/build.gradle
 * 3. Añade RNGestureHandlerPackage al MainApplication
 */

const path = require("path");
const { withSettingsGradle, withAppBuildGradle, withDangerousMod } = require("@expo/config-plugins");

/** @type {import("@expo/config-plugins").ConfigPlugin} */
function withGestureHandler(config) {
  config = withSettingsGradle(config, (config) => {
    if (!config.modResults.contents.includes(":react-native-gesture-handler")) {
      const nodeScript = `require('path').join(require('path').dirname(require.resolve('react-native-gesture-handler/package.json', {paths: [require.resolve('react-native/package.json')]})), 'android')`;
      config.modResults.contents += `
// @generated by with-gesture-handler plugin (autolinking no lo incluye en monorepo pnpm)
include ':react-native-gesture-handler'
project(':react-native-gesture-handler').projectDir = new File(
  ["node", "--print", "${nodeScript.replace(/"/g, '\\"')}"].execute(null, rootDir).text.trim()
)
`;
    }
    return config;
  });

  config = withAppBuildGradle(config, (config) => {
    if (!config.modResults.contents.includes("react-native-gesture-handler")) {
      config.modResults.contents = config.modResults.contents.replace(
        /dependencies\s*\{/,
        `dependencies {
        implementation project(':react-native-gesture-handler')`
      );
    }
    return config;
  });

  config = withDangerousMod(config, [
    "android",
    async (config) => {
      const platformRoot = config.modRequest.platformProjectRoot;
      const rootBuildGradlePath = path.join(platformRoot, "build.gradle");
      const fs = require("fs");
      let rootGradle = fs.readFileSync(rootBuildGradlePath, "utf8");
      if (!rootGradle.includes("REACT_NATIVE_NODE_MODULES_DIR")) {
        rootGradle = rootGradle.replace(
          'apply plugin: "com.facebook.react.rootproject"',
          `apply plugin: "com.facebook.react.rootproject"

// Monorepo pnpm: react-native está en la raíz del workspace
rootProject.ext.REACT_NATIVE_NODE_MODULES_DIR = file("\${rootDir}/../../../node_modules/react-native")`
        );
        fs.writeFileSync(rootBuildGradlePath, rootGradle);
      }
      return config;
    },
  ]);

  // RNGestureHandlerPackage ya está en PackageList vía autolinking; no modificar MainApplication

  return config;
}

module.exports = withGestureHandler;
